def move(n, x=1, y=3):
    if n == 1:
        print("Перенести диск 1 со стержня", x, "на стержень", y)
    else:
        x_n = (6 - x) - y
        move(n - 1, x, x_n)
        print("Перенести диск", n, "со стержня", x, "на стержень", y)
        pos = (n - 1, x_n, y)
        print("Перенести диск", n - 1, "со стержня", x_n, "на стержень", y)
        return pos


n_d = int(input('Введите количество дисков: '))
